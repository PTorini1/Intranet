    function CreateSection(month){
        for(let x = 1; x <= 31; x++){
            if(month[x]==""){
            
            }else if (typeof(month[x]) == "object"){
                for(let y=0;y < month[x].length; y++){
                    let div1 = window.document.createElement("div")
                    div1.className = "pricing-entry d-flex ftco-animate"

                    let div2 = window.document.createElement("div")
                    div2.className = "img"
                    div2.setAttribute('name', 'icone-aniversario')

                    let div3 = window.document.createElement("div")
                    div3.className = "desc pl-3"

                    let div4 = window.document.createElement("div")
                    div4.className = "d-flex text align-items-center"

                    let h3Nome = window.document.createElement("h3")
                    let h3Dia = window.document.createElement("h3") 
                    h3Dia.className = "dia"

                    h3Nome.innerText = month[x][y]
                    h3Dia.innerText = x
                    
                    col.appendChild(div1)
                    div1.appendChild(div2)
                    div1.appendChild(div3)
                    div3.appendChild(div4)
                    div4.appendChild(h3Nome)
                    div4.appendChild(h3Dia)
                }
            }else if(month[x] == undefined || month[x] == null){
                
            }else{
                let div1 = window.document.createElement("div")
                div1.className = "pricing-entry d-flex ftco-animate"

                let div2 = window.document.createElement("div")
                div2.className = "img"
                div2.setAttribute('name', 'icone-aniversario')

                let div3 = window.document.createElement("div")
                div3.className = "desc pl-3"

                let div4 = window.document.createElement("div")
                div4.className = "d-flex text align-items-center"

                let h3Nome = window.document.createElement("h3")
                h3Nome.innerText = String(month[x])
                
                let h3Dia = window.document.createElement("h3") 
                h3Dia.className = "dia"
                

                    col.appendChild(div1)
                    div1.appendChild(div2)
                    div1.appendChild(div3)
                    div3.appendChild(div4)
                    div4.appendChild(h3Nome)
                    div4.appendChild(h3Dia)
                    h3Dia.textContent = String(x)

            }
        }
    }